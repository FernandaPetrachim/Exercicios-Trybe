let saldo = 1500;

function somarSaldo(saldo, somar){
return saldo + somar;    
}
saldo = somarSaldo(saldo, 200)
console.log (saldo);

function subtrairValor(saldo, subtrair){
return saldo - subtrair;
}
saldo = subtrairValor(saldo, 500)
console.log (saldo);

function multiplicarValor(saldo, multiplicar){
return saldo * multiplicar;
}
saldo = multiplicarValor (saldo, 300)
console.log(saldo);

function dividirValor(saldo, dividir){
return saldo / dividir;
}
saldo = dividirValor(saldo, 400)
console.log(saldo);











